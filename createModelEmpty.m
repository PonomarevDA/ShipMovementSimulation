%======================================================================
%> @brief Create empty model
%======================================================================
function model = createModelEmpty()
model.Type = "-";
model.Variant = "-";
model.Group = "-";
model.Name = "-";
model.W = "-";                          % displacement, ton
model.N = "-";                          % power, horsepower
model.V = "-";                       	% speed, knots
model.Vk = "-";                         % speed, knots
model.V1 = "-";                         % speed, knots
model.V2 = "-";                         % speed, knots
end

